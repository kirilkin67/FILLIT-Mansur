void	ft_set_coord1(int *a, int t, int m)
{
	if (t == 103 || t == 111 || t == 110 || t == 105 || t == 114 || t == 119)
		a[0] = 1;
	else if (t == 101)
		a[0] = 0;
	else if (t == 108 || t == 106 || t == 109 || t == 112 || t == 118)
		a[0] = 2;
	else if (t == 102)
		a[0] = 3;
	else if (t == 107 || t == 113 || t == 116)
		a[0] = 2 + m;
	else if (t == 115 || t == 117)
		a[0] = 1 + m;
	else if (t == 104)
		a[0] = 1 + 2 * m;
}

